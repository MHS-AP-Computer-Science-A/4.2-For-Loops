
public class Main {

	public static void main(String[] args) {
		// For loops define a loop variable,
		// provide a looping condition based on that variable,
		// and define how to iterate the variable
		
		//Count from 0 to 9 by 1s
		for (int i = 0; i < 5; i++) {
			System.out.println(i);
		}
		
		//Count from 100 to 0 by 2s
		for (int i = 100; i >=0; i-=2) {
			System.out.println(i);
		}
	}

}
